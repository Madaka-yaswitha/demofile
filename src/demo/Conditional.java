package demo;

public class Conditional {

	public static void main(String[] args) {
		int day =4;
		 switch(day) {
		 
		 case 1 : System.out.println(" 1 Rank");
		 break;
		 
		 case 2 : System.out.println(" 2 Rank");
		 break;
		 
		 case 3: System.out.println(" 3 Rank");
		 break;
		 
		 case 4 : System.out.println(" 4 Rank");
		 break;
		 
		 default : System.out.println(" Enter  correct value");
		 break;
		 
		 
		 
		 }

	}

}
