package demo;

public class Arithmetic {
	 public static void main(String []args) {
		 int a = 30;
		 int b = 40;
		 int c = 28;
		 int d = 31;
		  System.out.println("a + b = " + (a + b) );
		  System.out.println("a - b = " + (a - b) );
		  System.out.println("a * b = " + (a * b) );
		  System.out.println("b / a = " + (b / a) );
		  System.out.println("b % a = " + (b % a) );
		  System.out.println("c % a = " + (c % a) );
		  System.out.println("a++ = " + (a++) );
		  System.out.println("a--  = " + (a--) );
		  
		  System.out.println("d++ = " + (d++) );
		  System.out.println("++d = " + (++d) );
	 }

}
